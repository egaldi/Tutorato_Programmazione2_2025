/*
Quali delle seguenti affermazioni descrivono correttamente una funzione di ordine superiore?

A. È una funzione che può essere chiamata più di una volta.
B. È una funzione che può restituire un’altra funzione.
C. È una funzione che accetta come parametro un puntatore a funzione.
D. È una funzione che utilizza ricorsione.
E. È una funzione che accede a variabili globali.

*/




/*SOLUZIONI

A. Falso – Tutte le funzioni possono essere chiamate più volte, non è una proprietà delle funzioni di ordine superiore.

B. Vero – Una funzione di ordine superiore può restituire una funzione.

C. Vero – Anche accettare una funzione come argomento la rende di ordine superiore.

D. Falso – La ricorsione non ha nulla a che vedere con l’ordine della funzione.

E. Falso – Anche le funzioni "normali" possono accedere a variabili globali, non è una caratteristica distintiva.

*/